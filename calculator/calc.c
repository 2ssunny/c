#include <stdio.h>
#include <math.h>

int main()
{
    int input; // common
    int choice, a, b, sum;
    int primeinput; //소수판별
    float d;        //소수판별
    int ranf, rant, h, k, l, m, n, o, p;
    float j; //소수 범위

    printf("선택\n1:계산기\n2:짝홀 판별기\n3:소수판별\n4:범위내 소수\n:");
    scanf("%d", &input);

    if (input == 1) //사칙연산
    {
        printf("덧셈은 1, 뺄셈은 2, 곱셈은 3, 나눗셈은 4를 입력하세요.\n");
        scanf("%d", &choice);

        if (choice == 1)
        { //덧셈
            printf("첫번째 수 입력");
            scanf("%d", &a);
            printf("두번째 수 입력");
            scanf("%d", &b);
            sum = a + b;
            printf("결과는 %d", sum);
        }

        if (choice == 2)
        { //뺄셈
            printf("첫번째 수 입력");
            scanf("%d", &a);
            printf("두번째 수 입력");
            scanf("%d", &b);
            sum = a - b;
            printf("결과는 %d", sum);
        }

        if (choice == 3)
        { //곱셈
            printf("첫번째 수 입력");
            scanf("%d", &a);
            printf("두번째 수 입력");
            scanf("%d", &b);
            sum = a * b;
            printf("결과는 %d", sum);
        }

        if (choice == 4)
        { //나눗셈
            printf("첫번째 수 입력");
            scanf("%d", &a);
            printf("두번째 수 입력");
            scanf("%d", &b);
            sum = a / b;
            printf("결과는 %d", sum);
        }
    }
    if (input == 2) //짝홀 판별기
    {
        printf("검사할 수를 입력하세요\n");
        scanf("%d", &choice);
        a = choice % 2;
        if (a == 0)
        { //짝수
            printf("입력한 수는 짝수입니다.");
        }
        if (a == 1)
        { //홀수
            printf("입력한 수는 홀수입니다.");
        }
    }
    if (input == 3) //소수 판별기
    {
        printf("판별할 수를 입력하세요\n");
        scanf("%d", &primeinput);
        d = sqrt(primeinput);
        a = (int)d;
        for (int i = 1; i <= primeinput; i++) //소수 판별
        {
            sum = primeinput % a;
        }

        if (sum != 0)
        { //소수 아님
            printf("입력한 수는 소수가 아닙니다.");
        }
        if (sum == 0)
        { //소수
            printf("입력한 수는 소수입니다.");
        }
    }
    if (input == 4) //범위내 소수_수정 필요
    {
        printf("범위를 시작할 수를 입력하세요");
        scanf("%d", &ranf);
        printf("범위를 끝낼 수를 입력하세요");
        scanf("%d", &rant);
        printf("2\n3\n5\n7\n");
        for (int i = ranf; i <= rant; i++)
        {
            i != 2, 3, 5, 7;
            j = sqrt(i);
            int g = (int)j;
            h = i % g;
            if (h != 0)
            {
                k = i % 2;
                if (k != 0)
                {
                    l = i % 3;
                    if (l != 0)
                    {
                        m = i % 5;
                        if (m != 0)
                        {
                            n = i % 7;
                            if (n != 0)
                            {
                                printf("%d\n", i);
                            }
                        }
                    }
                }
            }
        }
    }
}